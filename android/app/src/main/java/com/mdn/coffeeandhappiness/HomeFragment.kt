        videoView.start()

        videoView.setOnPreparedListener(object : MediaPlayer.OnPreparedListener {
            override fun onPrepared(mp: MediaPlayer) {
                // The video is prepared, you can start playback here if needed
                mp.isLooping = true
            }
        })

